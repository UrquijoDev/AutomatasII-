 private void compilar() {
    txtA_salida.setText("");
    analizadorLexico();
    llenarTablaTokens();
    
    // Verificar si hay tokens antes del análisis sintáctico
    if (tokens.isEmpty()) {
        txtA_salida.append("Error: El archivo está vacío.\n");
        return;
    }
    
    if(!lexer.errorFound){
        analizadorSintactico();
    } 
}
